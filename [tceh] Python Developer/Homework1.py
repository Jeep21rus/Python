# Напишите программу, которая спрашивает у пользователя два числа и знак: "+" или "-".
# В зависимости от знака выводит их сумму или разницу
num1 = float(input("Введите первое число: "))
num2 = float(input("Введите второе число: "))
operation = input("Введите знак операции (+ или -): ")
if operation == '+':
    result = num1 + num2
elif operation == '-':
    result = num1 - num2
else:
    print("Введен неподдерживаемый знак операции. Поддерживаются только + и -.")
    exit()
print("Результат: ", result)

# Напишите программу, которая находит все простые числа между 0 и пользовательским числом
print('Простые числа между 0 и Вашим числом: ')
upper_value = int(input('Введите число: '))
prime_found = False
for number in range(2, upper_value + 1):
    for i in range(2, number):
        if(number % i) == 0:
            break
    else:
        print(number)
        prime_found = True
if not prime_found:
    print('Простых чисел между 0 и Вашим числом нет')

