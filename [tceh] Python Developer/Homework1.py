# Задача: реализовать игру в загадки
# Требования:
# Программа выводить в консоль текст загадки и ждать ввода пользователя
# Программа после ввода пользователя ответа должна вывести в консоль результат:
# правильный ли ответ дал пользователь
# Загадок должно быть 10, тематика вопросов должна быть по первому занятию
# Дополнительные требования (со звездочкой или сложные, необязательно для выполнения):
# Программа должна в конце игры сказать, сколько ответов дал пользователь: сколько из них было верных
# Программа должна не учитывать регистр ответа: "Python" и "python" оба должны быть
# правильным ответом на вопрос "Какой язык мы учим?"
riddles = {
    'Какой язык мы учим?': ['python', 'Python', 'питон', 'Питон'],
    'Как зовут преподователя?': ['Никита', 'никита'],
    'Какой тип данных используется для хранения текста?': ['string', 'String', 'строка', 'Строка', 'str'],
    'Ключевое слово для начала цикла с предусловием?': ['while'],
    'Ключевое слово для преобразования в целочисленный тип данных?': ['int'],
    'Ключевое слово для преобразования в численный тип данных с плавающей точкой?': ['float'],
    'Каким ключевым словом в Пайтон обозначается Истина?': ['True'],
    'Каким ключевым словом в Пайтон обозначается Неизвестность / Пустота?': ['None'],
    'Ключевое слово для начала перебирающего цикла?': ['for'],
    'Ключевое слово для вывода значения на экран?': ['print'],
}

correct_answers = 0

for riddle, answers in riddles.items():
    user_answer = input(riddle + "\n")
    if user_answer.lower() in [answer.lower() for answer in answers]:
        print("Верно!")
        correct_answers += 1
    else:
        print("Неверно!")

print(f"Вы ответили правильно на {correct_answers} из {len(riddles)} загадок.")
